/* Zoek alle invul-zinnen voor één meervoudsvorm. */
window.SpellingMeervoudZinnen.zoekVoor = function(meervoudWoord, graad) {
  if (!meervoudWoord) return null;
  graad = graad || 1;
  const bib = window.SpellingMeervoudZinnen[`graad${graad}`];
  if (!bib) return null;
  const key = (typeof meervoudWoord === "string") 
    ? meervoudWoord.toLowerCase() 
    : (meervoudWoord.meervoud || "").toLowerCase();
  const zinnen = bib[key];
  return (zinnen && zinnen.length > 0) ? zinnen : null;
};

/* Selecteer willekeurig N zinnen voor N meervoudswoorden uit een lijst.
   Returns: [{ meervoud, grondwoord, zin }, ...]
   Stop-condities: leverancierslijst leeg of N bereikt of geen zinnen meer. */
window.SpellingMeervoudZinnen.willekeurigeSet = function(woorden, aantal, seedFn) {
  const rng = seedFn || Math.random;
  // Filter woorden waarvoor zinnen bestaan
  const beschikbaar = woorden.filter(w => {
    const mv = (w.meervoud || w).toString().toLowerCase();
    return window.SpellingMeervoudZinnen.zoekVoor(mv);
  });
  // Schud
  const kopie = [...beschikbaar];
  for (let i = kopie.length - 1; i > 0; i--) {
    const j = Math.floor(rng() * (i + 1));
    [kopie[i], kopie[j]] = [kopie[j], kopie[i]];
  }
  // Neem N
  const gekozen = kopie.slice(0, Math.min(aantal, kopie.length));
  return gekozen.map(w => {
    const meervoud = (w.meervoud || w).toString().toLowerCase();
    const grondwoord = w.tekst || meervoud;
    const zinnen = window.SpellingMeervoudZinnen.zoekVoor(meervoud);
    const zinIdx = Math.floor(rng() * zinnen.length);
    return {
      meervoud: meervoud,
      grondwoord: grondwoord,
      zin: zinnen[zinIdx]
    };
  });
};

/* Stat voor debugging */
window.SpellingMeervoudZinnen.statistiek = function(graad) {
  graad = graad || 1;
  const bib = window.SpellingMeervoudZinnen[`graad${graad}`];
  if (!bib) return { woorden: 0, zinnen: 0 };
  const woorden = Object.keys(bib).length;
  const zinnen = Object.values(bib).reduce((s, arr) => s + arr.length, 0);
  return { woorden, zinnen };
};
