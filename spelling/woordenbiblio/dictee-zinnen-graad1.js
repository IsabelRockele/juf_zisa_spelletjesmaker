/* Sommige groepen zijn pedagogisch verschillend maar produceren
   dezelfde dictee-vorm: een zin voor "verdubbel-verenkel|kat" past
   ook bij "meervouden|kat" (beide tonen "de katten"). Met deze
   synoniem-tabel hoef je de zin maar één keer in te vullen. */
window.SpellingDicteeZinnen._synoniem = {
  "meervouden": "verdubbel-verenkel",
  "verdubbel-verenkel": "meervouden"
};

/* Zoek de dictee-zin voor één woord op basis van grondvorm + groep.
   Probeert eerst de exacte groep, dan een synoniem-groep. Geeft null
   terug als er nog geen zin is — de weekdictee-module valt dan terug
   op een auto-sjabloon. */
window.SpellingDicteeZinnen.zoekVoor = function(grondvorm, groep, graad) {
  if (!grondvorm || !groep) return null;
  graad = graad || 1;
  const bib = window.SpellingDicteeZinnen["graad" + graad];
  if (!bib) return null;

  // Probeer eerst de exacte (groep, grondvorm)-key
  const direct = bib[groep + "|" + grondvorm];
  if (direct && typeof direct === "string") return direct;

  // Probeer synoniem-groep
  const syn = window.SpellingDicteeZinnen._synoniem[groep];
  if (syn) {
    const indirect = bib[syn + "|" + grondvorm];
    if (indirect && typeof indirect === "string") return indirect;
  }
  return null;
};

/* Stat voor debugging — hoeveel zinnen zijn al ingevuld? */
window.SpellingDicteeZinnen.statistiek = function(graad) {
  graad = graad || 1;
  const bib = window.SpellingDicteeZinnen["graad" + graad];
  if (!bib) return { totaal: 0, ingevuld: 0, todo: 0 };
  const keys = Object.keys(bib);
  const ingevuld = keys.filter(k => typeof bib[k] === "string" && bib[k].length > 0).length;
  return { totaal: keys.length, ingevuld: ingevuld, todo: keys.length - ingevuld };
};