function shuffle(array) {
  const copy = [...array];
  for (let i = copy.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy;
}

function uniekeSignatuur(woorden) {
  return [...woorden].sort((a, b) => a.localeCompare(b)).join("||");
}

function maakHoofdletter(zin) {
  if (!zin) return "";
  return zin.charAt(0).toUpperCase() + zin.slice(1);
}

function formatteerTekstje(item, niveau) {
  if (!item || !item.zin) return "";

  let nieuw = item.zin.trim().replace(/[.!?]+$/g, "");

  if (niveau === "M3") {
    return nieuw + ".";
  }

  if (niveau === "E3") {
    return maakHoofdletter(nieuw) + ".";
  }

  const metHoofdletter = maakHoofdletter(nieuw);

  if (item.type === "vraag") {
    return metHoofdletter + "?";
  }

  if (item.type === "roep") {
    return metHoofdletter + "!";
  }

  return metHoofdletter + ".";
}

function kiesTekstjesZonderDirecteHerhaling(tekstjes, aantal) {
  if (!tekstjes || !tekstjes.length) return [];

  const resultaat = [];
  let vorige = "";

  while (resultaat.length < aantal) {
    const pool = shuffle(tekstjes);

    for (const zin of pool) {
      if (resultaat.length >= aantal) break;
      if (zin === vorige && pool.length > 1) continue;
      resultaat.push(zin);
      vorige = zin;
    }
  }

  return resultaat.slice(0, aantal);
}

function kiesGetypeerdeTekstjesZonderDirecteHerhaling(tekstjesObj, aantal) {
  if (!tekstjesObj) return [];

  const types = ["zin", "vraag", "roep"];
  const resultaat = [];
  let vorigeVolledigeZin = "";

  for (let i = 0; i < aantal; i++) {
    const type = types[i % types.length];
    const lijst = tekstjesObj[type] || [];

    if (!lijst.length) {
      resultaat.push({ type: "zin", zin: "" });
      continue;
    }

    let pool = shuffle([...lijst]);
    let gekozen = pool.find((zin) => zin !== vorigeVolledigeZin) || pool[0];

    resultaat.push({ type, zin: gekozen });
    vorigeVolledigeZin = gekozen;
  }

  return resultaat;
}

function maakUniekeKaarten(woordenlijst, aantalKaartjes, woordenPerKaart) {
  const kaarten = [];
  const gebruikteSignaturen = new Set();
  const maxPogingen = 5000;
  let pogingen = 0;

  while (kaarten.length < aantalKaartjes && pogingen < maxPogingen) {
    const gemixt = shuffle(woordenlijst);
    const gekozen = gemixt.slice(0, woordenPerKaart);
    const signatuur = uniekeSignatuur(gekozen);

    if (!gebruikteSignaturen.has(signatuur)) {
      gebruikteSignaturen.add(signatuur);
      kaarten.push(gekozen);
    }

    pogingen += 1;
  }

  return {
    kaarten,
    volledigGelukt: kaarten.length === aantalKaartjes
  };
}

